//转义字符的使用
public class change {
	public static void main(String[] args) {

	 // \t :一个制表位，实现对其的功能
	    System.out.println("zzj\tzzj\tzzj");
	 // \n：换行符  
	    System.out.println("jack\nrose");
	 // \\：一个\
	    System.out.println("c\\v\\b"); 
	 // \" ：一个"
	    System.out.println("I said: \"I love learning programming.\""); 
	 // \r: return
	    System.out.println("a\tb\tc\n1\t2\t3");
	} 
}
